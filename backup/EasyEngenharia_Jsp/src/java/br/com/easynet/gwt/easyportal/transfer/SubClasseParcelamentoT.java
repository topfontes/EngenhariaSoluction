public class SubClasseParcelamentoT {
    private int plc_nr_id;
    private int par_nr_id;
    private int intervalo;
    private int numero_parcelas;

    /**
     * @return the plc_nr_id
     */
    public int getPlc_nr_id() {
        return plc_nr_id;
    }

    /**
     * @param plc_nr_id the plc_nr_id to set
     */
    public void setPlc_nr_id(int plc_nr_id) {
        this.plc_nr_id = plc_nr_id;
    }

    /**
     * @return the par_nr_id
     */
    public int getPar_nr_id() {
        return par_nr_id;
    }

    /**
     * @param par_nr_id the par_nr_id to set
     */
    public void setPar_nr_id(int par_nr_id) {
        this.par_nr_id = par_nr_id;
    }

    /**
     * @return the intervalo
     */
    public int getIntervalo() {
        return intervalo;
    }

    /**
     * @param intervalo the intervalo to set
     */
    public void setIntervalo(int intervalo) {
        this.intervalo = intervalo;
    }

    /**
     * @return the numero_parcelas
     */
    public int getNumero_parcelas() {
        return numero_parcelas;
    }

    /**
     * @param numero_parcelas the numero_parcelas to set
     */
    public void setNumero_parcelas(int numero_parcelas) {
        this.numero_parcelas = numero_parcelas;
    }

}
